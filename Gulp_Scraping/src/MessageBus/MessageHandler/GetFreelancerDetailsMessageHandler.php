    /**
     * GetFreelancerDetailsMessageHandler constructor.
     *
     * @param LoggerInterface $logger
     * @param GulpClient $client
     */

    public function __construct(LoggerInterface $logger, GulpClient $client)
    {
        $this->logger = $logger;
        $this->client = $client;
    }

    private function codeToCountry($codeList)
    {
        $countries = array();

        foreach ($codeList as $code) {
            $code = strtoupper($code);

            if (in_array("Deutschland", $countries)) {
                if (self::countryList[$code] != 'Deutschland') {
                    if (self::countryList[$code]) {
                        array_push($countries, self::countryList[$code]);
                    }
                }
            } else {
                if (self::countryList[$code]) {
                    array_push($countries, self::countryList[$code]);
                }
            }
        }

        return $countries;
    }

    /**
     * @param GetFreelancerDetailsMessage $message
     */
    public function __invoke(GetFreelancerDetailsMessage $message)
    {
        try {
            // TODO Fetch Details from Freelancer and parse them
            $freelancerDetails = $this->client->getClient()->request('GET', $message->getHref());
            $content = json_decode($freelancerDetails->getContent());

            $freelancer = [
                'userId' => $content->expert->id,
                'image' => $content->expert->profilePictureUrl,
                "title" => $content->profile->coreCompetence,
                "availability" => [
                    "from" => $content->profile->availableFrom
                ],
                "price" => [
                    "type" => "hourly",
                    "amount" => $content->profile->expectedPayment->rate,
                ],
                "countries" => $this->codeToCountry($content->profile->regions),
                "areas" => [
                    "zipcodes" => $content->profile->regions,
                    "surroundings" => $content->profile->locations
                ],
                "projects" => $content->profile->projects,
                "languages" => $content->profile->foreignLanguages,
                "tags" => $content->profile->allCompetencesWithSynonyms
            ];

            if ($message->getId()) {
                if (!file_exists('var/profiles/')) {
                    mkdir('var/profiles/', 0777, true);
                }

                file_put_contents("var/profiles/" . $message->getId() . ".json", json_encode($freelancer));
            } else {
                if (!file_exists('var/log/json_output.log')) {
                    mkdir('var/log/json_output.log', 0777, true);
                }
                $error_message = "Today (" . date('d-M-Y') .  "), Profile views of your account exceeds the limit of numbers allowed. You have to try it tomorrow or use another account.\r\n";
                $this->logger->error(sprintf($error_message));
                error_log($error_message, 3, "var/log/json_error.log");
            }

        } catch (\Exception $e) {
            $this->logger->error(sprintf("Error parsing Freelancer: %s (Code: %s, Message: %s)", $message->getHref(), $e->getCode(), $e->getMessage()), $e->getTrace());
        }
    }
